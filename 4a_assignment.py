# Create a dictionary with an (imaginary) person with attributes like his name, age and hobbies

# create the dictionary from an empty dict
person = {}
person['name'] = 'James'
person['age'] = 30
person['hobbies'] = ['football', 'tennis', 'walking']

# create the dictionary with all the keys and values immediately
person = {'name': 'James',
          'age': 30,
          'hobbies': ['football', 'tennis', 'walking']}

print(person)